#!/bin/bash
# run_onepage_experiments.sh
# Run one-page experiments for a defended dataset tag.
#
# Usage:
#   bash run_onepage_experiments.sh <TAG>
#   bash run_onepage_experiments.sh <TAG> <PAGES_DIR_NAME>
#
# Examples:
#   bash run_onepage_experiments.sh padl1_pin0p005_pout0p005_L0_G0
#   bash run_onepage_experiments.sh mix_K4_deltat0p01_N3 CW_mix_K4_deltat0p01_N3_pages
#
# Default expected input directory:
#   datasets/CW_tam_<TAG>_pages/
#
# Optional override:
#   datasets/<PAGES_DIR_NAME>/
#
# Behavior:
#   1) Reads one-page .npz files from the pages directory
#   2) Temporarily copies each page .npz into datasets/ for dataset_split.py compatibility
#   3) Runs split/TAM/train/test as usual
#   4) Moves the generated working directory:
#        datasets/<DATASET_NAME>/
#      into:
#        <PAGES_DIR>/<DATASET_NAME>/
#   5) Deletes the temporary root-level page .npz copy:
#        datasets/<DATASET_NAME>.npz

# Relax strict error handling so one failing model/page does not kill the whole run
# set -euo pipefail
set -u

if [ $# -lt 1 ]; then
    echo "Usage: bash run_onepage_experiments.sh <TAG> [PAGES_DIR_NAME]"
    echo "Example 1: bash run_onepage_experiments.sh padl1_pin0p005_pout0p005_L0_G0"
    echo "Example 2: bash run_onepage_experiments.sh mix_K4_deltat0p01_N3 CW_mix_K4_deltat0p01_N3_pages"
    exit 1
fi

TAG="$1"
IN_BASE="datasets"

# Default keeps old Tamaraw behavior; optional second argument lets you pass
# the actual page-folder name for non-Tamaraw datasets like CW_mix_*_pages.
PAGES_DIR_NAME="${2:-CW_tam_${TAG}_pages}"
PAGES_DIR="${IN_BASE}/${PAGES_DIR_NAME}"

LOG_DIR="logs_onepage/${TAG}"

mkdir -p "${LOG_DIR}"

echo "============================================"
echo "Running one-page experiments for TAG = ${TAG}"
echo "Input pages directory: ${PAGES_DIR}"
echo "Logs directory: ${LOG_DIR}"
echo "============================================"

if [ ! -d "${PAGES_DIR}" ]; then
    echo "ERROR: ${PAGES_DIR} not found."
    echo "Generate one-page page datasets first."
    exit 1
fi

shopt -s nullglob
PAGE_FILES=("${PAGES_DIR}"/*.npz)

if [ ${#PAGE_FILES[@]} -eq 0 ]; then
    echo "ERROR: No .npz page files found in ${PAGES_DIR}"
    exit 1
fi

for PAGE_FILE in "${PAGE_FILES[@]}"; do
    PAGE_BASENAME=$(basename "${PAGE_FILE}")
    DATASET_NAME="${PAGE_BASENAME%.npz}"

    # Temporary root-level copy used by the existing dataset scripts.
    ROOT_PAGE_NPZ="${IN_BASE}/${DATASET_NAME}.npz"

    # Working directory generated by dataset_split.py / gen_tam.py.
    PAGE_WORK_DIR="${IN_BASE}/${DATASET_NAME}"

    # Final archive location inside the pages directory.
    PAGE_ARCHIVE_DIR="${PAGES_DIR}/${DATASET_NAME}"

    echo "--------------------------------------------"
    echo "Processing ${DATASET_NAME}"
    echo "--------------------------------------------"

    # Copy for dataset_split.py compatibility
    cp "${PAGE_FILE}" "${ROOT_PAGE_NPZ}"

    # Split
    python exp/dataset_process/dataset_split.py \
        --dataset "${DATASET_NAME}" \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_split.log" || true

    # Generate TAM for RF
    for split in train valid test; do
        python -u exp/dataset_process/gen_tam.py \
            --dataset "${DATASET_NAME}" \
            --seq_len 5000 \
            --in_file "${split}" \
            2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_tam_${split}.log" || true
    done

    # DF - batch_size 512 / test 1024
    python -u exp/train.py --dataset "${DATASET_NAME}" --model DF \
        --device cuda:0 --feature DIR --seq_len 5000 \
        --train_epochs 30 --batch_size 512 --learning_rate 2e-3 \
        --optimizer Adamax \
        --eval_metrics Accuracy Precision Recall F1-score \
        --save_metric F1-score --save_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_DF_train.log" || true

    python -u exp/test.py --dataset "${DATASET_NAME}" --model DF \
        --device cuda:0 --feature DIR --seq_len 5000 \
        --batch_size 1024 \
        --eval_metrics Accuracy Precision Recall F1-score TPR FPR \
        --load_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_DF_test.log" || true

    # TikTok - batch_size 512 / test 1024
    python -u exp/train.py --dataset "${DATASET_NAME}" --model TikTok \
        --device cuda:0 --feature DT --seq_len 5000 \
        --train_epochs 30 --batch_size 512 --learning_rate 2e-3 \
        --optimizer Adamax \
        --eval_metrics Accuracy Precision Recall F1-score \
        --save_metric F1-score --save_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_TikTok_train.log" || true

    python -u exp/test.py --dataset "${DATASET_NAME}" --model TikTok \
        --device cuda:0 --feature DT --seq_len 5000 \
        --batch_size 1024 \
        --eval_metrics Accuracy Precision Recall F1-score TPR FPR \
        --load_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_TikTok_test.log" || true

    # VarCNN - batch_size 256 / test 512
    python -u exp/train.py --dataset "${DATASET_NAME}" --model VarCNN \
        --device cuda:0 --feature DT2 --seq_len 5000 \
        --train_epochs 30 --batch_size 256 --learning_rate 1e-3 \
        --optimizer Adam \
        --eval_metrics Accuracy Precision Recall F1-score \
        --save_metric F1-score --save_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_VarCNN_train.log" || true

    python -u exp/test.py --dataset "${DATASET_NAME}" --model VarCNN \
        --device cuda:0 --feature DT2 --seq_len 5000 \
        --batch_size 512 \
        --eval_metrics Accuracy Precision Recall F1-score TPR FPR \
        --load_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_VarCNN_test.log" || true

    # RF - batch_size 512 / test 1024
    python -u exp/train.py --dataset "${DATASET_NAME}" --model RF \
        --device cuda:0 --train_file tam_train --valid_file tam_valid \
        --feature TAM --seq_len 1800 \
        --train_epochs 30 --batch_size 512 --learning_rate 5e-4 \
        --optimizer Adam \
        --eval_metrics Accuracy Precision Recall F1-score \
        --save_metric F1-score --save_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_RF_train.log" || true

    python -u exp/test.py --dataset "${DATASET_NAME}" --model RF \
        --device cuda:0 --test_file tam_test \
        --feature TAM --seq_len 1800 \
        --batch_size 1024 \
        --eval_metrics Accuracy Precision Recall F1-score TPR FPR \
        --load_name max_f1 \
        2>&1 | tee "${LOG_DIR}/${DATASET_NAME}_RF_test.log" || true

    # Move the generated per-page working directory into the pages archive.
    if [ -d "${PAGE_WORK_DIR}" ]; then
        rm -rf "${PAGE_ARCHIVE_DIR}"
        mv "${PAGE_WORK_DIR}" "${PAGES_DIR}/"
    else
        echo "WARNING: Expected working directory ${PAGE_WORK_DIR} was not found."
    fi

    # Remove the temporary root-level .npz copy created for compatibility.
    rm -f "${ROOT_PAGE_NPZ}"

    echo "Finished ${DATASET_NAME}"
    echo
done

echo "============================================"
echo "Finished one-page experiments for TAG = ${TAG}"
echo "============================================"
